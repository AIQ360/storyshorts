create extension if not exists "uuid-ossp";

create table if not exists public.video_jobs (
  id uuid not null default uuid_generate_v4() primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  status text not null default 'queued',
  progress numeric not null default 0,
  input_json jsonb not null default '{}'::jsonb,
  output_json jsonb not null default '{}'::jsonb,
  error text,
  idempotency_key text,
  trigger_run_id text,
  engine_job_id text,
  required_credits bigint not null default 0,
  consumed_credits bigint not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists video_jobs_user_id_idempotency_key_unique
  on public.video_jobs(user_id, idempotency_key)
  where idempotency_key is not null;

create table if not exists public.video_job_events (
  id bigint generated by default as identity primary key,
  job_id uuid not null references public.video_jobs(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  event_type text not null,
  payload jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

alter table public.video_jobs enable row level security;
alter table public.video_job_events enable row level security;

drop policy if exists "Users can read own video_jobs" on public.video_jobs;
create policy "Users can read own video_jobs"
  on public.video_jobs for select
  using (auth.uid() = user_id);

drop policy if exists "Users can insert own video_jobs" on public.video_jobs;
create policy "Users can insert own video_jobs"
  on public.video_jobs for insert
  with check (auth.uid() = user_id);

drop policy if exists "Users can update own video_jobs" on public.video_jobs;
create policy "Users can update own video_jobs"
  on public.video_jobs for update
  using (auth.uid() = user_id);

drop policy if exists "Users can read own video_job_events" on public.video_job_events;
create policy "Users can read own video_job_events"
  on public.video_job_events for select
  using (auth.uid() = user_id);

drop policy if exists "Users can insert own video_job_events" on public.video_job_events;
create policy "Users can insert own video_job_events"
  on public.video_job_events for insert
  with check (auth.uid() = user_id);

