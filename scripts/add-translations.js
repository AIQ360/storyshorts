// Merge function: add missing keys to a locale object
function mergeTranslations(localeData, newKeys) {
  for (const section of Object.keys(newKeys)) {
    if (localeData[section] === undefined) {
      localeData[section] = {};
    }
    for (const key of Object.keys(newKeys[section])) {
      if (localeData[section][key] === undefined) {
        localeData[section][key] = newKeys[section][key];
      }
    }
  }
  return localeData;
}

for (const locale of Object.keys(translations)) {
  const filePath = path.join(messagesDir, locale + ".json");
  const data = JSON.parse(fs.readFileSync(filePath, "utf8"));
  const merged = mergeTranslations(data, translations[locale]);
  fs.writeFileSync(filePath, JSON.stringify(merged, null, 2) + "\n", "utf8");
  console.log(`Updated ${locale}.json`);
}
